package com.Entity;

import java.util.ArrayList;
import java.util.List;

public class ArcticSet {
	private List<Arctic> ArctictoList = new ArrayList<Arctic>();
	
	public ArcticSet(List<Arctic> Arcticlist){
		this.ArctictoList = Arcticlist;
	}
	
	
	public int getSize(){
		return ArctictoList.size();
	}
	public List<Arctic> getArctictoList() {
		return ArctictoList;
	}


	public void setArctictoList(List<Arctic> arctictoList) {
		ArctictoList = arctictoList;
	}


	public Arctic getItem(int arg0){
		return ArctictoList.get(arg0);
	}
}
