package com.Entity;

import java.util.ArrayList;
import java.util.List;

public class MerchantSet {

private List<Merchant> MerchanList = new ArrayList<Merchant>();
	
	public MerchantSet(List<Merchant> Merchanlist){
		this.MerchanList = Merchanlist;
	}
	
	
	
	public List<Merchant> getMerchanList() {
		return MerchanList;
	}



	public void setMerchanList(List<Merchant> merchanList) {
		MerchanList = merchanList;
	}



	 public int getSize(){
		 
		return MerchanList.size();
	}


	public Merchant getItem(int arg0){
		return MerchanList.get(arg0);
		}
}
