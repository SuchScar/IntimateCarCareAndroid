package com.Entity;

import java.util.ArrayList;
import java.util.List;

public class WeiZhangSet {
private List<WeiZhang> WeiZhangList = new ArrayList<WeiZhang>();
	
	public WeiZhangSet(List<WeiZhang> WeiZhanglist){
		this.WeiZhangList = WeiZhanglist;
	}
	
	
	public int getSize(){
		return WeiZhangList.size();
	}

	public List<WeiZhang> getWeiZhangList() {
		return WeiZhangList;
	}


	public void setWeiZhangList(List<WeiZhang> weiZhangList) {
		WeiZhangList = weiZhangList;
	}


	public WeiZhang getItem(int arg0){
		return WeiZhangList.get(arg0);
	}
}
